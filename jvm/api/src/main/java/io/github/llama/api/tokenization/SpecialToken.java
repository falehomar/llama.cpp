package io.github.llama.api.tokenization;

/**
 * Enum representing special tokens.
 */
public enum SpecialToken {
    /**
     * Beginning of sequence token.
     */
    BOS,

    /**
     * End of sequence token.
     */
    EOS,

    /**
     * Padding token.
     */
    PAD,

    /**
     * Unknown token.
     */
    UNK
}
